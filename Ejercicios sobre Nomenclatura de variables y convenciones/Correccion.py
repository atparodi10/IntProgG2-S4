# Versión original | Convención a usar
# a) nombrepersona → camelCase
# b) EdadPersona → snake_case
# c) TotalPrecio → camelCase
# d) numero-de-telefono → snake_case
# e) DireccionEmail → camelCase

nombrePersona = "Juan"
print("nombrePersona:", nombrePersona)
print("Convención: camelCase\n")


edad_persona = 25
print("edad_persona:", edad_persona)
print("Convención: snake_case\n")

totalPrecio = 100.50
print("totalPrecio:", totalPrecio)
print("Convención: camelCase\n")

numero_de_telefono = "123-456-7890"
print("numero_de_telefono:", numero_de_telefono)
print("Convención: snake_case\n")

direccionEmail = "correo@ejemplo.com"
print("direccionEmail:", direccionEmail)
