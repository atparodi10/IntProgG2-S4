# A partir de las siguientes descripciones, escribe el nombre de una variable que siga
# una convención lógica y clara. Puedes elegir la convención, pero justificarla.
# 1. La edad del usuario
# 2. El precio total del carrito de compras
# 3. El estado de inicio de sesión (activo o inactivo)
# 4. El nombre del curso actual
# 5. La temperatura promedio registrada

# 1. La edad del usuario – snake_case
edad_usuario = 25
print("edad_usuario:", edad_usuario)
print("Convención: snake_case")
print("Justificación: Python recomienda snake_case para variables, y es clara y legible.\n")

# 2. El precio total del carrito de compras – snake_case
precio_total_carrito = 199.99
print("precio_total_carrito:", precio_total_carrito)
print("Convención: snake_case")
print("Justificación: Es un nombre largo, y snake_case mejora su comprensión visual.\n")

# 3. El estado de inicio de sesión – camelCase
sesionActiva = True
print("sesionActiva:", sesionActiva)
print("Convención: camelCase")
print("Justificación: En booleanos cortos, camelCase funciona bien y es muy usado en frontend.\n")

# 4. El nombre del curso actual – snake_case
nombre_curso_actual = "Matemática Básica"
print("nombre_curso_actual:", nombre_curso_actual)
print("Convención: snake_case")
print("Justificación: Nombre largo y descriptivo, ideal para snake_case por claridad.\n")

# 5. La temperatura promedio registrada – camelCase
temperaturaPromedio = 22.7
print("temperaturaPromedio:", temperaturaPromedio)
print("Convención: camelCase")
print("Justificación: También válida en muchos entornos, especialmente si vienes de otros lenguajes como JavaScript.\n")
