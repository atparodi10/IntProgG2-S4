# . Declara nombres de variables para los siguientes casos, aplicando la convención indicada.
# 1. Nombre completo del estudiante – camelCase
# 2. Número de matrícula – snake_case
# 3. Total de ventas del mes – PascalCase
# 4. Límite máximo de intentos – SCREAMING_SNAKE_CASE
# 5. Cantidad de productos – camelCase


nombreCompletoEstudiante = "Ana María"
print("nombreCompletoEstudiante:", nombreCompletoEstudiante)
print("Convención: camelCase\n")

numero_matricula = "2025A001"
print("numero_matricula:", numero_matricula)
print("Convención: snake_case\n")

TotalVentasDelMes = 15320.75
print("TotalVentasDelMes:", TotalVentasDelMes)
print("Convención: PascalCase\n")

LIMITE_MAXIMO_INTENTOS = 5
print("LIMITE_MAXIMO_INTENTOS:", LIMITE_MAXIMO_INTENTOS)
print("Convención: SCREAMING_SNAKE_CASE\n")

cantidadProductos = 42
print("cantidadProductos:", cantidadProductos)
