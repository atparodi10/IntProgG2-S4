# . El dueño de una tienda compra un artículo a un precio determinado. Obtener el precio
# en que lo debe vender para obtener una ganancia del 30%.

precio_costo = float(input("Ingrese el precio de costo del artículo: "))

precio_venta = precio_costo * 1.30

print(f"El precio de venta con una ganancia del 30% es: {precio_venta}")