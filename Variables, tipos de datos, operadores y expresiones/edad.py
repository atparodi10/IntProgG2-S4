# Pide una edad actual y muestra qué edad tendrá el usuario dentro de 5, 10 y 20 años.

edad_actual = int(input("Ingrese su edad actual: "))

edad_en_5 = edad_actual + 5
edad_en_10 = edad_actual + 10
edad_en_20 = edad_actual + 20

print(f"En 5 años tendrá: {edad_en_5} años")
print(f"En 10 años tendrá: {edad_en_10} años")
print(f"En 20 años tendrá: {edad_en_20} años")